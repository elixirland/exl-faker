  @adjective_count tuple_size(@adjectives)
  @default_probability 0.5

  def prepend_random_adjective(list, opts) do
    probability = Keyword.get(opts, :probability, @default_probability)

    if :rand.uniform() < probability do
      prepend_random_adjective(list)
    else
      list
    end
  end

  def prepend_random_adjective(list) do
    [random_adjective() | list]
  end

  def random_adjective do
    elem(
      @adjectives,
      :rand.uniform(@adjective_count) - 1
    )
  end
